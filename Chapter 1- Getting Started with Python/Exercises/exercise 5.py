# Prompt the user to input the radius of a circle and store the value in the variable 'r'.
r = int(input("enter the circle radius"))
# Calculate the area of the circle using the formula A = π * r^2, where 'π' is approximated as 22/7.
area = 22/7 * r * r
# Print a message to display the calculated area along with the input radius.
print("the area of the given circle radius is", area)
