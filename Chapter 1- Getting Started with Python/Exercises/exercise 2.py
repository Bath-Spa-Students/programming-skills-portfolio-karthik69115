# Import the sys module to access system-related information
import sys
# Print a header to indicate that we are displaying Python version information
print("Python version")
# Print the full Python version string, including build and release information
print(sys.version)
# Print a header to indicate that we are displaying Python version info in a structured way
print("Version info.")
# Print the Python version information as a structured tuple
print(sys.version_info)
